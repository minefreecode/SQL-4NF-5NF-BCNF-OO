DROP TABLE ONE;
DROP TABLE TWO;

CREATE TABLE ONE
(MANUFACTURER VARCHAR2(25) NOT NULL,
ITEM VARCHAR(25) NOT NULL,
PRIMARY KEY (MANUFACTURER,ITEM));

CREATE TABLE TWO
(MANUFACTURER VARCHAR2(25) NOT NULL,
DELIVERY_AREA VARCHAR(25) NOT NULL,
PRIMARY KEY (MANUFACTURER,DELIVERY_AREA));

