from STORE_CUSTOMERS a, MANUFACTURED_ITEMS b
WHERE a.MANUFACTURER_NAME = b.MANUFACTURER_NAME;

DELETE FROM STORE_CUSTOMERS
WHERE CUSTOMER_NAME = 'Ben'
AND MANUFACTURER_NAME = 'Kitchen World';

