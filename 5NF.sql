/*5NF Example*/

DROP TABLE R1;
DROP TABLE R2;
DROP TABLE R3;

CREATE TABLE R1
(MANUFACTURER VARCHAR2(25),
ITEM VARCHAR2(25),
PRIMARY KEY (MANUFACTURER,ITEM));

CREATE TABLE R2
(ITEM VARCHAR2(25),
SALESPERSON VARCHAR2(25),
PRIMARY KEY (ITEM,SALESPERSON));

CREATE TABLE R3
(MANUFACTURER VARCHAR2(25),
SALESPERSON VARCHAR2(25),
PRIMARY KEY (MANUFACTURER,SALESPERSON));
INSERT INTO R1 VALUES ('Kitchen World','Doughnut');
