Select * from R1,R2
WHERE R1.ITEM = R2.ITEM;

Select * from R1,R3
WHERE R1.MANUFACTURER = R3.MANUFACTURER;

Select t3.MANUFACTURER,t3.SALESPERSON,t1.ITEM
FROM R3 t3, R2 t2, R1 t1
WHERE t1.MANUFACTURER = t3.MANUFACTURER
AND t2.SALESPERSON = t3.SALESPERSON
AND t1.ITEM = t2.ITEM;
